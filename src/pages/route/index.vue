<template>
  <div>
    <placeInput />

    <scroll-view class="scroll" scroll-y enable-back-to-top scroll-with-animation>
      <history />
      <share />
    </scroll-view>
  </div>
</template>

<script>
import placeInput from "./cell/placeInput";
import history from "./cell/history";
import share from "@/components/share";

import store from "@/store";

export default {
  data() {
    return {};
  },

  components: {
    placeInput,
    history,
    share
  },

  methods: {},

  created() {
    // load current place
    store.commit("route/clearPlace");
    wx.getLocation({
      type: "gcj02",
      success: res => {
        store.commit("route/updatePlace", {
          which: "a",
          place: {
            name: "My location",
            latitude: res.latitude,
            longitude: res.longitude
          }
        });
      }
    });
  },

  onShareAppMessage(res) {
    return {
      title: "Urban Public Transpotation",
      path: "/pages/route/main",
      imageUrl: "/static/image/logo.png"
    };
  }
};
</script>

